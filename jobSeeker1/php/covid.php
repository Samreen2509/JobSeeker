    <h2>Here for you during COVID-19</h2> 
    <p>The new coronavirus is officially called SARS-CoV-2, which stands for severe acute respiratory syndrome <br>coronavirus 2.
         An infection with this virus can lead to coronavirus disease 19, or COVID-19.<br><br>
         The evidence shows that SARS-CoV-2 may be transmitted more easily and cause life-threatening illness in some people.<br><br>
         Like other coronaviruses, it can survive in the air and on surfaces long enough for someone to contract it
    </p><br><br>
</div>
<hr><hr>
<div id="middleArea3">
    <h1>Tips For Preventation...</h1><br>
    <p>Follow the guidelines to help protect yourself from contracting and transmitting SARS-CoV-2.</p><br><br>
    <h2>1. Wash your hands frequently and carefully</h2><br>
    <p>Use warm water and soap and rub your hands for at least 20 seconds. Work the lather to your wrists, between your fingers, and under your
         fingernails. You can also use an antibacterial and antiviral soap.</p><br><br>
         <h2>2. Avoid touching your face</h2><br>
         <p> SARS-CoV-2 can live on some surfaces for up to 72 hours. You can get the virus on your hands if you touch a surface like:</p>
         <ul>
             <li>gas pump handle</li>
             <li>your cell phone</li>
             <li>a doorknob</li>
</ul><br>
<h2>3. Stop shaking hands and hugging people — for now</h2>
<p>Similarly, avoid touching other people. Skin-to-skin contact can transmit SARS-CoV-2 <br>
    from one person to another.</p><br>
<h2>4.Don’t share personal items  </h2>
<p>Don't share personal item like</p>
<ul>
             <li>Phones</li>
             <li>Makeup</li>
             <li>Comb</li>
</ul><br>
<p>It’s also important not to share eating utensils and straws. Teach children to recognize their reusable cup,<br> 
    straw, and other dishes for their own use only.</p> <br>

    <h2> 5. Clean and disinfect surfaces</h2><p>Use alcohol-based disinfectants to clean hard surfaces in your home like:</p>
    <ul>
             <li>countertops</li>
             <li>door handles</li>
             <li>furniture</li>
             <li>toys</li>
</ul><br>
<h2>6.  Cover your mouth and nose when you cough and sneeze</h2>
<p>SARS-CoV-2 is found in high amounts in the nose and mouth. This means it can be carried by <br>
    air droplets to  other people when you cough, sneeze, or talk. 
    It can also<br>
     land on hard surfaces and stay there for up to 3 days.<br><br>
     Use a tissue or sneeze into your elbow to keep your hands as clean as possible. Wash your hands <br>
     carefully after you sneeze or cough, regardless.</p> <br>
     <h2>7. Take physical (social) distancing seriously</h2>
     <p>If you’re carrying the SARS-CoV-2 virus, it’ll be found in high amounts in your spit (sputum). This can happen<br>
      even if you don’t have symptoms.</p><br>
    <h2> 8. Do not gather in groups</h2>
    <p>Being in a group or gathering makes it more likely that you’ll be in close contact with someone.<br>

This includes avoiding all religious places of worship, as you may 
have to sit or stand too close to another congregant.
 It also includes not congregating at parks or beaches.</p><br>
 <h2>9. Avoid eating or drinking in public places</h2>
 <p>Now is not the time to go out to eat. This means avoiding restaurants, coffee shops, bars, and<br> other eateries.
<br>The virus can be transmitted through food, utensils, dishes, and cups. It may also be temporarily airborne from other<br> people in
 the venue.<br>
 You can still get delivery or takeaway food. Choose foods that are thoroughly cooked and can be reheated.<br>
 High heat (at least 132°F/56°C, according to one recent, not-yet-peer-reviewed lab study) helps to kill coronaviruses.<br>
 This means it may be best to avoid cold foods from restaurants and all food from buffets and open salad bar<br></p>
<br>
<h2>10. Wash fresh groceries</h2>
<p>Wash all produce under running water before eating or preparing.<br><br>
The CDC Trusted Source and the FDA Trusted Source do not recommend using soap, detergent, or commercial produce wash on things <br>
like fruits and vegetables. Be sure to
 wash hands before and after handling these items.</p><br>
 <h2>11.  Wear a (homemade) mask</h2>
 <p>The Centers for Disease Control and Prevention (CDC) recommendsTrusted Source that almost everyone wears a cloth face mask <br>in public 
     settings where physical distancing may be difficult, such as grocery stores.<br>
     When used correctly, these masks can help prevent people who are asymptomatic or undiagnosed from transmitting SARS-CoV-2 when they breathe, 
     <br>talk, sneeze, or cough. This, in turn, slows the transmission of the virus.<br>
     Some pointers to keep in mind:<br><br></p>
     <ul>
             <li>Wearing a mask alone will not prevent you from getting a SARS-CoV-2 infection. Careful handwashing and physical<br>
              distancing must also be followed.</li>
             <li>Cloth masks aren’t as effective as other types of masks, such as surgical masks or N95 respirators. However, these<br>
              other masks should be reserved for healthcare workers and first responders.</li>
             <li>Wash your hands before you put on your mask.</li>
             <li>Wash your mask after each use.</li>
             <li>You can transfer the virus from your hands to the mask. If you’re wearing a mask, avoid touching the front of it.</li>
             <li>You can also transfer the virus from the mask to your hands. Wash your hands if you touch the front of the mask.</li>
             <li>A mask shouldn’t be worn by a child under 2 years old, a person who has trouble breathing, or a person who can’t <br>remove the mask
                 on their own.</li>
</ul><br>
<h2>12.  Self-quarantine if sick</h2>
<p>Call your doctor if you have any symptoms. Stay home until you recover. Avoid sitting, sleeping, or eating with your loved ones even if you live <br>
in the same home.<br><br>
Wear a mask and wash your hands as much as possible. If you need urgent medical care, wear a mask and let them know <br>you may have COVID-19.</p>
